    public  void hienAllSanPham(){
        DefaultTableModel model = (DefaultTableModel) this.tbSanPham.getModel();
        Connection con =null;
        Statement stmt = null;
        try {
            con = DBConnect.getConnection();
            stmt = con.createStatement();
            String sql = "Select * from quanlisanpham";
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                model.addRow(new Object[]{
                rs.getString("maSanPham"),
                rs.getString("tenSanPham"),
                rs.getDouble("donGia"),
                rs.getString("donVi")
                });
            }
            con.close();
            rs.close();
            stmt.close();
        } catch (SQLException ex) {
             Logger.getLogger(SuaXoaSP.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
