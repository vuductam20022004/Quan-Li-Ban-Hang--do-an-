    private void buttonTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTimKiemActionPerformed
        // TODO add your handling code here:
        Connection con = null;
        Statement stmt = null;
        DefaultTableModel model = (DefaultTableModel) this.tbSanPham.getModel();
        //xoa du lieu bang
        for (int i =  tbSanPham.getRowCount()-1; i>=0; i--) {
            model.removeRow(i);
        }
        try {
            con = DBConnect.getConnection();
            stmt = con.createStatement();
            String sql ="Select * from quanlisanpham where maSanPham='"+txtMaSanPham.getText()+"' or tenSanPham ='"+txtTenSanPham.getText()+"'";
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                model.addRow(new Object[]{
                rs.getString("maSanPham"),
                rs.getString("tenSanPham"),
                rs.getDouble("donGia"),
                rs.getString("donVi")
                });
            }
            con.close();
            stmt.close();
            rs.close();
        } catch (SQLException ex) {
            System.out.println("oks");
            Logger.getLogger(SuaXoaSP.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_buttonTimKiemActionPerformed

    private void buttonXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonXoaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) this.tbSanPham.getModel();
        Connection con = null;
        PreparedStatement stmt = null;
        int rowSelect = tbSanPham.getSelectedRow();
        //vừa xóa vừa cập nhật  vào trong cơ sở dữ liệu.
        if (rowSelect != -1) {
             model.removeRow(rowSelect);
             //update vào cơ sở dữ liệu.
             try {
                con = DBConnect.getConnection();
                String sql = "delete from quanlisanpham where maSanPham ='"+txtMaSanPham.getText()+"'";
                stmt = con.prepareStatement(sql);
                stmt.executeUpdate();
                con.close();
                stmt.close();
                JOptionPane.showMessageDialog(rootPane, "Xóa sản phẩm thành công.");
                txtMaSanPham.setText("");
                txtTenSanPham.setText("");
                txtDonGia.setText("");
                txtDonVi.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(SuaXoaSP.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else {
            JOptionPane.showMessageDialog(rootPane, "Chọn mục muốn thực hiện!");
            return;
        }
    }//GEN-LAST:event_buttonXoaActionPerformed

    private void buttonChinhSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonChinhSuaActionPerformed
        // TODO add your handling code here:
        Double dongia = (Double.valueOf(txtDonGia.getText()));
        
        DefaultTableModel model = (DefaultTableModel) this.tbSanPham.getModel();
        Connection con = null;
        PreparedStatement stmt = null;
        int rowSelect = tbSanPham.getSelectedRow();
        //xóa ở trên bảng và trong cơ ở dữ liệu
        if (rowSelect != -1) {
             model.removeRow(rowSelect);
             //update vào cơ sở dữ liệu.
             try {
                con = DBConnect.getConnection();
                String sql = "delete from quanlisanpham where maSanPham ='"+txtMaSanPham.getText()+"'";
                stmt = con.prepareStatement(sql);
                stmt.executeUpdate();
                con.close();
                stmt.close();
            } catch (SQLException ex) {
                Logger.getLogger(SuaXoaSP.class.getName()).log(Level.SEVERE, null, ex);
            }
             //cập nhật kết quả vừa sửa vào cơ sở dữ liệu
            try {
                con = DBConnect.getConnection();
                String sql1 = "insert into quanlisanpham(maSanPham,tenSanPham,donGia,donVi) values('"+txtMaSanPham.getText()+"' ,'"+txtTenSanPham.getText()+"','"+txtDonGia.getText()+"','"+txtDonVi.getText()+"')";
                
                stmt = con.prepareStatement(sql1);
                stmt.executeUpdate();
                con.close();
                stmt.close();
            } catch (SQLException ex) {
                Logger.getLogger(SuaXoaSP.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else {
            JOptionPane.showMessageDialog(rootPane, "Chọn mục muốn thực hiện!");
            return;
        }
        //hiển thị lại kq vừa sửa lên bảng
        model.addRow(new Object[]{
        txtMaSanPham.getText(),
        txtTenSanPham.getText(),
        dongia,
        txtDonVi.getText()
        });
        JOptionPane.showMessageDialog(rootPane, "Thành công.");
        txtMaSanPham.setText("");
        txtTenSanPham.setText("");
        txtDonGia.setText("");
        txtDonVi.setText("");
    }//GEN-LAST:event_buttonChinhSuaActionPerformed
